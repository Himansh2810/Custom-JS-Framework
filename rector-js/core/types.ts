type Attrs<T extends keyof HTMLElementTagNameMap> = Partial<
  Omit<HTMLElementTagNameMap[T], keyof HTMLElement>
> &
  DOMEventProps<HTMLElementTagNameMap[T]> & {
    /** CSS classes to apply to the element */
    class?: string;
    /** Unique ref generated by useElementRef to select this element later. */
    ref?: ElementRef<HTMLElementTagNameMap[T]>;
    children?: HTMLElement | HTMLElement[];
    style?: CSSStyleDeclaration;
  };

type TagName = keyof HTMLElementTagNameMap;
type RectorElements = {
  [K in TagName]: (attributes: Attrs<K>) => HTMLElementTagNameMap[K];
};

