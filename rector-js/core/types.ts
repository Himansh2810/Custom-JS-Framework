type EventHandler<T extends EventTarget = EventTarget> = (
  e: SyntheticEvent<T>,
) => void;

type SyntheticEventHandler<
  T extends HTMLElement,
  K extends keyof HTMLElementEventMap,
> = (e: SyntheticEvent<T>) => void;

type DOMEventProps<T extends HTMLElement> = {
  [K in keyof HTMLElementEventMap as `on${K}`]?: SyntheticEventHandler<T, K>;
};

