type StateUsageConfig =
  | {
      type: "condition";
      config: IfBlockConfig;
    }
  | {
      type: "loop";
      config: LoopBlockConfig;
    }
  | {
      type: "child";
      config: StateUseObj;
    }
  | {
      type: "attr";
      config: AttrUseObj;
    };

type Block = {
  componentRendered: string[];
  stateUsageCleanUps: (() => void)[];
  stateUsageRefIds: number[];
};

type State<T> = {
  readonly value: T;
  set(val: T | ((prev: T) => T)): void;
};

type List<T> = {
  readonly value: readonly T[];
  readonly length: number;
  set(val: T[] | ((prev: readonly T[]) => T[])): void;
  update(index: number, value: T): void;
  insert(index: number, ...values: T[]): void;
  push(...values: T[]): void;
  unshift(...values: T[]): void;
  pop(): void;
  shift(): void;
  remove(index: number): void;
  removeRange(start: number, end: number): void;
};

type BatchTypes = "insert" | "update" | "remove" | "removeRange";

type RenderBatch =
  | {
      type: "set";
      index?: number;
      value?: any;
      state: State<any> | List<any>;
    }
  | {
      type: BatchTypes;
      index?: number;
      value?: any;
      state: List<any>;
    };

